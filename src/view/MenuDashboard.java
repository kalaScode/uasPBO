    private void loadDashboardData() {
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;

        try {
            conn = DatabaseManager.getInstance().getConnection();
            LocalDate today = LocalDate.now();
            String todayString = today.toString();  
            System.out.println("Today's Date: " + todayString);  

            String sqlTotalEvent = "SELECT COUNT(*) FROM event";
            stmt = conn.prepareStatement(sqlTotalEvent);
            rs = stmt.executeQuery();
            if (rs.next()) {
                int totalEvent = rs.getInt(1);
                labelTotalEvent.setText(String.valueOf(totalEvent));
                System.out.println("Total Event: " + totalEvent); 
            }
            rs.close();
            stmt.close();

            String sqlTotalPeserta = "SELECT COUNT(*) FROM peserta";
            stmt = conn.prepareStatement(sqlTotalPeserta);
            rs = stmt.executeQuery();
            if (rs.next()) {
                int totalPeserta = rs.getInt(1);
                LabelTotalPeserta.setText(String.valueOf(totalPeserta));
                System.out.println("Total Peserta: " + totalPeserta);  
            }
            rs.close();
            stmt.close();

            String sqlEventSelesai = "SELECT COUNT(*) FROM event WHERE tanggal < ?";
            stmt = conn.prepareStatement(sqlEventSelesai);
            stmt.setString(1, todayString); 
            rs = stmt.executeQuery();
            if (rs.next()) {
                int totalEventSelesai = rs.getInt(1);
                LabelStatus.setText(String.valueOf(totalEventSelesai));
                System.out.println("Total Event Selesai: " + totalEventSelesai);  
            }

        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("SQL Error: " + e.getMessage());  
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                e.printStackTrace();
                System.out.println("Closing Error: " + e.getMessage());  
            }
        }
    }
    
    private void loadEventTable() {
        EventController eventController = new EventController();
        List<Event> events = eventController.getAllEvents();

        PesertaController pesertaController = new PesertaController();
        List<Peserta> allParticipants = pesertaController.getAllPeserta();

        for (Event event : events) {
            event.updateJumlahPendaftar(allParticipants);
        }

        DefaultTableModel model = new DefaultTableModel(new Object[]{"Nama Event", "Deskripsi", "Tanggal", "Kuota", "Jumlah Pendaftar", "Status"}, 0);
        tabelEventDashboard.setModel(model);

        for (Event event : events) {
            Object[] row = {
                event.getNamaEvent(),
                event.getDeskripsi(),
                event.getTanggal(),
                event.getKuota(),
                event.getJumlahPendaftar(),
                event.getStatus()
            };
            model.addRow(row);
        }
    }
    
    
