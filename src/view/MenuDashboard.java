    private void displayRowDetails(int row) {
            String namaEvent = tabelEventDashboard.getModel().getValueAt(row, 0).toString();
            String deskripsi = tabelEventDashboard.getModel().getValueAt(row, 1).toString();
            String tanggal = tabelEventDashboard.getModel().getValueAt(row, 2).toString();
            String kuota = tabelEventDashboard.getModel().getValueAt(row, 3).toString();
            String jumlahPendaftar = tabelEventDashboard.getModel().getValueAt(row, 4).toString();
            String status = tabelEventDashboard.getModel().getValueAt(row, 5).toString();

            javax.swing.JPanel panelDetail = new javax.swing.JPanel();
            panelDetail.setLayout(new GridLayout(0, 1));

            panelDetail.add(new javax.swing.JLabel("Nama Event: " + namaEvent));
            panelDetail.add(new javax.swing.JLabel("Deskripsi: " + deskripsi));
            panelDetail.add(new javax.swing.JLabel("Tanggal: " + tanggal));
            panelDetail.add(new javax.swing.JLabel("Kuota: " + kuota));
            panelDetail.add(new javax.swing.JLabel("Jumlah Pendaftar: " + jumlahPendaftar));
            panelDetail.add(new javax.swing.JLabel("Status: " + status));

            JOptionPane.showMessageDialog(null, panelDetail, "Detail Event", JOptionPane.INFORMATION_MESSAGE);
        }


